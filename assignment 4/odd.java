class odd
{
	public static void main(String args[])
	{
		System.out.print("The odd numbers between 1 to 100 are:");
		for(int i=1;i<=100;i=i+2)
		{
			
			System.out.println(+i);	
		}
		System.out.print("The reverse order from 300 to 3 are:");
		for(int j=300;j>=3;j=j-3)
		{
			
			System.out.println(+j);
		}
	}
}
